package entity;

import java.util.Objects;

/**
 *
 * @author KHONG TAM
 */
public class Categoryes {
    private String id;
    private String name;

    public Categoryes(String id) {
        this.id = id;
    }

    public Categoryes(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(String id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public int hashCode() {
        int hash = 7;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Categoryes other = (Categoryes) obj;
        return Objects.equals(this.id, other.id);
    }
    
    
}
